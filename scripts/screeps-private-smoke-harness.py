def build_readme(runtime_dir: Path, secret_presence: SecretPresence, map_cached: bool) -> str:
    map_status = "present" if map_cached else "missing"
    return textwrap.dedent(
        f"""\
        # Private Server Smoke Runtime Workspace

        Generated by `scripts/screeps-private-smoke-harness.py prepare`.

        This directory is ignored by git. It may contain local secrets, downloaded maps,
        Docker volumes, and transient API tokens. Do not copy values from this directory
        into committed documentation.

        ## Current local readiness

        - Steam key: {secret_presence.summary}
        - Map cache `maps/{MAP_FILE}`: {map_status}
        - Docker is not started by this harness.

        ## Secret handling

        The generated `config.yml` uses `steamKeyFile: STEAM_KEY`. Before starting
        Docker, create `./STEAM_KEY` in this ignored directory or adapt the config
        locally. If `/root/.secret/.env` contains `STEAM_KEY`, copy it without shell
        tracing and without printing the value. The file should contain only the Steam
        Web API key. The harness never writes an environment-provided `STEAM_KEY`
        value to disk and never prints it.

        `STEAM_KEY.example` is a placeholder only.

        {build_plan(runtime_dir)}
        """
    )


def write_if_changed(path: Path, content: str) -> bool:
    existing = None
    try:
        existing = path.read_text(encoding="utf-8")
    except OSError:
        pass
    if existing == content:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return True


def download_map(map_path: Path, timeout: int) -> str:
    request = urllib.request.Request(MAP_URL, headers={"User-Agent": "screeps-private-smoke-harness/1.0"})
    try:
        with urllib.request.urlopen(request, timeout=timeout) as response:
            content = response.read()
    except (OSError, urllib.error.URLError) as exc:
        return f"map download skipped/failed: {type(exc).__name__}"

    if not content.lstrip().startswith(b"{"):
        return "map download skipped/failed: response was not JSON"

    map_path.parent.mkdir(parents=True, exist_ok=True)
    map_path.write_bytes(content)
    return f"downloaded {rel(map_path)}"


def command_prepare(args: argparse.Namespace) -> int:
    runtime_dir = Path(args.runtime_dir).resolve()
    maps_dir = runtime_dir / "maps"
    map_path = maps_dir / MAP_FILE
    runtime_dir.mkdir(parents=True, exist_ok=True)
    maps_dir.mkdir(parents=True, exist_ok=True)
    (runtime_dir / "volumes").mkdir(parents=True, exist_ok=True)

    map_result = "map download not requested"
    if args.download_map and not map_path.exists():
        map_result = download_map(map_path, args.timeout)
    elif args.download_map:
        map_result = f"map already present: {rel(map_path)}"

    secret_presence = detect_steam_key(runtime_dir)
    changed = []
    for path, content in [
        (runtime_dir / "docker-compose.yml", build_compose()),
        (runtime_dir / "config.yml", build_config()),
        (runtime_dir / "STEAM_KEY.example", "REDACTED_STEAM_WEB_API_KEY_GOES_HERE\n"),
        (maps_dir / "README.md", f"Place or download {MAP_FILE} here. Source: {MAP_URL}\n"),
        (runtime_dir / "README.md", build_readme(runtime_dir, secret_presence, map_path.exists())),
    ]:
        if write_if_changed(path, content):
            changed.append(rel(path))

    print(f"prepared {rel(runtime_dir)}")
    print(f"steam key: {secret_presence.summary}")
    print(map_result)
    if changed:
        print("updated:")
        for path in changed:
            print(f"  {path}")
    else:
        print("updated: no file changes")

    if not args.no_plan:
        print()
        print(build_plan(runtime_dir).rstrip())
    return 0


def command_plan(args: argparse.Namespace) -> int:
    print(build_plan(Path(args.runtime_dir).resolve()).rstrip())
    return 0


def assert_contains(haystack: str, needles: Iterable[str]) -> int:
    count = 0
    for needle in needles:
        if needle not in haystack:
            raise AssertionError(f"missing expected text: {needle}")
        count += 1
    return count


def assert_not_contains(haystack: str, needles: Iterable[str]) -> int:
    count = 0
    for needle in needles:
        if needle and needle in haystack:
            raise AssertionError("rendered template leaked a forbidden sentinel")
        count += 1
    return count


def command_self_test(_: argparse.Namespace) -> int:
    checks = 0
    config = build_config()
    compose = build_compose()
    plan = build_plan(DEFAULT_RUNTIME_DIR)
    readme = build_readme(
        DEFAULT_RUNTIME_DIR,
        SecretPresence(True, ("STEAM_KEY environment variable",)),
        map_cached=False,
    )

    checks += assert_contains(
        config,
        [
            "version: 4.2.21",
            "nodeVersion: Erbium",
            f"mapFile: {MAP_CONTAINER_PATH}",
            "steamKeyFile: STEAM_KEY",
        ],
    )
    checks += assert_contains(config, [f"  {name}: {version}" for name, version in PINNED_PACKAGES.items()])
    checks += assert_contains(config, [f"  - {mod}" for mod in MODS])
    checks += assert_contains(
        compose,
        [
            "screepers/screeps-launcher:latest",
            "mongo:8",
            "redis:7",
            '"127.0.0.1:21025:21025"',
            "./:/screeps",
        ],
    )
    checks += assert_contains(
        plan,
        [
            "docker compose up -d",
            f"utils.importMapFile('{MAP_CONTAINER_PATH}')",
            "system.resumeSimulation()",
            "/api/register/submit",
            "/api/user/code",
            "/api/game/place-spawn",
            "/api/user/overview",
            "mongosh screeps",
            "docker compose down --remove-orphans",
        ],
    )

    sentinel = "super-secret-steam-key-sentinel"
    leak_probe = config + compose + plan + readme
    checks += assert_not_contains(leak_probe, [sentinel, "STEAM_KEY=super-secret"])

    detected = detect_steam_key(DEFAULT_RUNTIME_DIR, {"STEAM_KEY": sentinel})
    if not detected.present or "STEAM_KEY environment variable" not in detected.sources:
        raise AssertionError("STEAM_KEY presence detection failed without reading or printing value")
    checks += 1

    temp_env = DEFAULT_RUNTIME_DIR / ".env"
    temp_env.parent.mkdir(parents=True, exist_ok=True)
    existing_temp_env = temp_env.read_bytes() if temp_env.exists() else None
    temp_env.write_text("STEAM_KEY=super-secret\n", encoding="utf-8")
    try:
        detected_file = detect_steam_key(DEFAULT_RUNTIME_DIR, {})
    finally:
        if existing_temp_env is None:
            temp_env.unlink(missing_ok=True)
        else:
            temp_env.write_bytes(existing_temp_env)
    if not detected_file.present or rel(temp_env) not in detected_file.sources:
        raise AssertionError("STEAM_KEY .env presence detection failed")
    checks += 1

    safe_fragment = re.sub(r"[^A-Za-z0-9_.-]+", "-", rel(DEFAULT_RUNTIME_DIR))
    if not safe_fragment:
        raise AssertionError("runtime directory relative path sanity check failed")
    checks += 1

    print(f"self-test passed ({checks} checks)")
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--runtime-dir",
        default=str(DEFAULT_RUNTIME_DIR),
        help="ignored runtime workspace to prepare or describe",
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    self_test = subparsers.add_parser("self-test", help="run offline harness checks")
    self_test.set_defaults(func=command_self_test)

    prepare = subparsers.add_parser("prepare", help="write ignored runtime files and print next steps")
    prepare.add_argument("--download-map", action="store_true", help="try to cache the pinned map JSON")
    prepare.add_argument("--timeout", type=int, default=30, help="map download timeout in seconds")
    prepare.add_argument("--no-plan", action="store_true", help="do not print the next-step command plan")
    prepare.set_defaults(func=command_prepare)

    plan = subparsers.add_parser("plan", help="print the manual next-step commands")
    plan.set_defaults(func=command_plan)
    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    try:
        return args.func(args)
    except BrokenPipeError:
        return 1
    except Exception as exc:  # noqa: BLE001 - keep CLI errors concise and secret-free
        print(f"error: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
