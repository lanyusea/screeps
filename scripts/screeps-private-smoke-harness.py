MAP_FILE = "map-0b6758af.json"
MAP_URL = f"https://maps.screepspl.us/maps/{MAP_FILE}"
MAP_CONTAINER_PATH = f"/screeps/maps/{MAP_FILE}"
SERVER_URL = "http://127.0.0.1:21025"
SMOKE_ROOM = "E1S1"
