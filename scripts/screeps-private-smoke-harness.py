@dataclass(frozen=True)
class SecretPresence:
    present: bool
    sources: tuple[str, ...]

    @property
    def summary(self) -> str:
        if not self.present:
            return "missing"
        return "present (" + ", ".join(self.sources) + ")"


def rel(path: Path) -> str:
    try:
        return str(path.relative_to(ROOT))
    except ValueError:
        return str(path)


def build_config() -> str:
    pinned = "\n".join(f"  {name}: {version}" for name, version in PINNED_PACKAGES.items())
    mods = "\n".join(f"  - {mod}" for mod in MODS)
    return (
        "# Generated by scripts/screeps-private-smoke-harness.py prepare.\n"
        "# This file is under ignored runtime-artifacts/. Do not commit local secrets.\n"
        "steamKeyFile: STEAM_KEY\n"
        "version: 4.2.21\n"
        "nodeVersion: Erbium\n"
        "pinnedPackages:\n"
        f"{pinned}\n"
        "mods:\n"
        f"{mods}\n"
        "serverConfig:\n"
        "  welcomeText: \"Local Screeps MVP pinned smoke server\"\n"
        "  tickRate: 200\n"
        f"  shardName: {SMOKE_SHARD}\n"
        f"  mapFile: {MAP_CONTAINER_PATH}\n"
    )


